
// 1. Número (Number): Representa tanto números inteiros quanto de ponto flutuante. Por exemplo:
let numero = 10;
let numeroDecimal = 3.14;


// 2. String: Sequência de caracteres. Pode ser criada com aspas simples (''), duplas ("") ou crases (``). Por exemplo:
let texto = 'Olá, mundo!';


// 3. Booleano (Boolean): Representa um valor verdadeiro ou falso. Por exemplo:
let verdadeiro = true;
let falso = false;


// 4. Valor Nulo (Null): Representa a ausência intencional de qualquer valor. Por exemplo:
let nulo = null;


// 5. Indefinido (Undefined): Representa uma variável que foi declarada, mas ainda não foi atribuída com um valor. Por exemplo:
let indefinido;